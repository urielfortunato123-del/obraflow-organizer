// Funﾃｧﾃ｣o para filtrar sugestﾃｵes baseado no input
export function filterSuggestions(input: string, list: string[]): string[] {
  if (!input || input.length < 1) return [];
  
  const normalizedInput = input
    .toUpperCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^A-Z0-9]/g, '');
  
  return list
    .filter(item => {
      const normalizedItem = item
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '')
        .replace(/[^A-Z0-9_]/g, '');
      return normalizedItem.includes(normalizedInput);
    })
    .slice(0, 10); // Limita a 10 sugestﾃｵes
}

